#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	if (!lst)
		return ;
	tmp = lst;
	while (1)
	{
		f(tmp->content);
		if (tmp->next == NULL)
			break ;
		tmp = tmp->next;
	}
}
